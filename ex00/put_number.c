void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void  ft_putnbr(unsigned int nb)
{
  if (nb >= 10)
   {
     ft_putnbr(nb / 10);
   }
  ft_putchar((nb % 10) + '0');
}